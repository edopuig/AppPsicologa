package com.appweb.psicologa.psicologa.model;

public class Blogs {
    private int idBlogs;
    private String titolBlog;
    private String url;
    private String extracteBlog;
    private int idUsuari;
    private String imgDestacadaBlog;
    private String tiupusBlog;
    
    public int getIdBlogs() {
        return idBlogs;
    }
    public void setIdBlogs(int idBlogs) {
        this.idBlogs = idBlogs;
    }
    public String getTitolBlog() {
        return titolBlog;
    }
    public void setTitolBlog(String titolBlog) {
        this.titolBlog = titolBlog;
    }
    public String getUrl() {
        return url;
    }
    public void setUrl(String url) {
        this.url = url;
    }
    public String getExtracteBlog() {
        return extracteBlog;
    }
    public void setExtracteBlog(String extracteBlog) {
        this.extracteBlog = extracteBlog;
    }
    public int getIdUsuari() {
        return idUsuari;
    }
    public void setIdUsuari(int idUsuari) {
        this.idUsuari = idUsuari;
    }
    public String getImgDestacadaBlog() {
        return imgDestacadaBlog;
    }
    public void setImgDestacadaBlog(String imgDestacadaBlog) {
        this.imgDestacadaBlog = imgDestacadaBlog;
    }
    public String getTiupusBlog() {
        return tiupusBlog;
    }
    public void setTiupusBlog(String tiupusBlog) {
        this.tiupusBlog = tiupusBlog;
    }
    
}
