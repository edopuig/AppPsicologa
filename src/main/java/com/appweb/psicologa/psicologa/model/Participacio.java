package com.appweb.psicologa.psicologa.model;

public class Participacio {
    private int idParticipacio;
    private int idAgenda;
    private int idUsuari;
    
    public int getIdParticipacio() {
        return idParticipacio;
    }
    public void setIdParticipacio(int idParticipacio) {
        this.idParticipacio = idParticipacio;
    }
    public int getIdAgenda() {
        return idAgenda;
    }
    public void setIdAgenda(int idAgenda) {
        this.idAgenda = idAgenda;
    }
    public int getIdUsuari() {
        return idUsuari;
    }
    public void setIdUsuari(int idUsuari) {
        this.idUsuari = idUsuari;
    }
    
}
