    private int idBlog;

    public int getIdContingut() {
        return idContingut;
    }
    public void setIdContingut(int idContingut) {
        this.idContingut = idContingut;
    }
    public String getTipusCongitnut() {
        return tipusCongitnut;
    }
    public void setTipusCongitnut(String tipusCongitnut) {
        this.tipusCongitnut = tipusCongitnut;
    }
    public String getContingut() {
        return contingut;
    }
    public void setContingut(String contingut) {
        this.contingut = contingut;
    }
    public int getIdBlog() {
        return idBlog;
    }
    public void setIdBlog(int idBlog) {
        this.idBlog = idBlog;
    }    
