package com.appweb.psicologa.psicologa.model;

public class BlogMetadata {
    private int idBlogMetadata;
    private int idBlog;
    private String clauBlogMetadata;
    private String valorMetadata;
    private String tiupusBlogMetadata;
    
    public int getIdBlogMetadata() {
        return idBlogMetadata;
    }
    public void setIdBlogMetadata(int idBlogMetadata) {
        this.idBlogMetadata = idBlogMetadata;
    }
    public int getIdBlog() {
        return idBlog;
    }
    public void setIdBlog(int idBlog) {
        this.idBlog = idBlog;
    }
    public String getClauBlogMetadata() {
        return clauBlogMetadata;
    }
    public void setClauBlogMetadata(String clauBlogMetadata) {
        this.clauBlogMetadata = clauBlogMetadata;
    }
    public String getValorMetadata() {
        return valorMetadata;
    }
    public void setValorMetadata(String valorMetadata) {
        this.valorMetadata = valorMetadata;
    }
    public String getTiupusBlogMetadata() {
        return tiupusBlogMetadata;
    }
    public void setTiupusBlogMetadata(String tiupusBlogMetadata) {
        this.tiupusBlogMetadata = tiupusBlogMetadata;
    }
    
}
